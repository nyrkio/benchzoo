// benchzoo canonical sample benchmark implemented for google/benchmark.
//
// Four tests, registered with explicit names "benchmark1".."benchmark4"
// so the reported JSON `name` field maps directly to the canonical
// `attributes["test_name"]` of the benchzoo data model. See
// ../../../docs/sample-benchmark.md for the specification.
//
// Notes on idiom:
//
// * Tests 1 and 4 are sleep-dominated and deterministic. We set
//   ->Iterations(1) on their registrations so google/benchmark does
//   exactly one measurement and does not attempt to multiply the
//   sleep to reach a longer minimum wall time. Without this the
//   library's autoscaling would run the sleep many times to hit its
//   ~0.5 s minimum and the captured value would no longer be "2.15 s".
//
// * Test 2 is a tight empty loop. An optimizing C++ compiler will
//   delete an empty loop outright; the canonical google/benchmark
//   idiom to prevent this is benchmark::DoNotOptimize on a value the
//   loop touches. We apply it to the loop counter inside the body so
//   the compiler cannot prove the loop has no observable effect.
//
// * Test 3 writes 1,400,000 bytes to /dev/null. The bytes are
//   generated by a trivial xorshift so we do not pull in any
//   dependency beyond the standard library. DoNotOptimize wraps the
//   ofstream reference after the write so the compiler cannot elide
//   the write as dead.

#include <benchmark/benchmark.h>

#include <chrono>
#include <cstdint>
#include <ctime>
#include <fstream>
#include <ios>
#include <thread>
#include <vector>

// Test 1 — sleep-dominated, ~2.15 s wall time, exactly one iteration.
static void BM_Benchmark1(benchmark::State& state) {
    for (auto _ : state) {
        std::this_thread::sleep_for(std::chrono::milliseconds(2150));
    }
}
BENCHMARK(BM_Benchmark1)
    ->Name("benchmark1")
    ->Unit(benchmark::kMillisecond)
    ->Iterations(1);

// Test 2 — tight CPU loop 0..1000 with DoNotOptimize to defeat the
// compiler's dead-code elimination.
static void BM_Benchmark2(benchmark::State& state) {
    for (auto _ : state) {
        for (int i = 0; i < 1000; ++i) {
            benchmark::DoNotOptimize(i);
        }
    }
}
BENCHMARK(BM_Benchmark2)
    ->Name("benchmark2")
    ->Unit(benchmark::kMillisecond);

// Test 3 — generate 1,400,000 bytes and write them to /dev/null.
// The xorshift is seeded from a constant so successive iterations are
// deterministic and so the captured fixture is reproducible.
static void BM_Benchmark3(benchmark::State& state) {
    constexpr std::size_t kSize = 1'400'000;
    std::vector<char> buffer(kSize);
    std::uint32_t x = 0x12345678u;
    for (std::size_t i = 0; i < kSize; ++i) {
        x ^= x << 13;
        x ^= x >> 17;
        x ^= x << 5;
        buffer[i] = static_cast<char>(x & 0xFFu);
    }

    for (auto _ : state) {
        std::ofstream out("/dev/null", std::ios::binary | std::ios::out);
        out.write(buffer.data(), static_cast<std::streamsize>(buffer.size()));
        benchmark::DoNotOptimize(out);
    }
}
BENCHMARK(BM_Benchmark3)
    ->Name("benchmark3")
    ->Unit(benchmark::kMillisecond);

// Test 4 — change-detection showcase. UTC month drives sleep length:
// sleep = 2.15 s + ((m mod 3) - 1) s. Exactly one iteration, same
// rationale as test 1.
static void BM_Benchmark4(benchmark::State& state) {
    for (auto _ : state) {
        const std::time_t now = std::chrono::system_clock::to_time_t(
            std::chrono::system_clock::now());
        std::tm utc{};
#if defined(_WIN32)
        gmtime_s(&utc, &now);
#else
        gmtime_r(&now, &utc);
#endif
        const int month = utc.tm_mon + 1;  // tm_mon is 0..11
        const int delta_ms = ((month % 3) - 1) * 1000;
        const int sleep_ms = 2150 + delta_ms;
        std::this_thread::sleep_for(std::chrono::milliseconds(sleep_ms));
    }
}
BENCHMARK(BM_Benchmark4)
    ->Name("benchmark4")
    ->Unit(benchmark::kMillisecond)
    ->Iterations(1);

BENCHMARK_MAIN();
